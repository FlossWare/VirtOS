// AI Attribution Formatting
// Creates consistent attribution format across ALL workflows
// Used by: auto-review-brutal, pr-review, code-solve, code-improve

export function formatAIAttribution(reviews, arbiterDecision, options = {}) {
  const {
    includeVerboseDetails = true,
    includeTimestamp = true,
  } = options

  const { opus, sonnet, haiku, gemini } = reviews

  let attribution = `## 🤖 AI Attribution

**Multi-Model Consensus Review**

### Reviewer Models
- **Opus**: ${formatModelDecision(opus)}
- **Sonnet**: ${formatModelDecision(sonnet)}
- **Haiku**: ${formatModelDecision(haiku)}`

  if (gemini) {
    attribution += `\n- **Gemini**: ${formatModelDecision(gemini)}`
  }

  attribution += `

### Arbiter Decision
- **Final Decision**: ${arbiterDecision.final_decision}
- **Consensus Score**: ${arbiterDecision.consensus_score}% agreement
- **Best Analysis**: ${arbiterDecision.accepted_model}
- **Reasoning**: ${arbiterDecision.accepted_reasoning}
`

  if (arbiterDecision.rejected_models && arbiterDecision.rejected_models.length > 0) {
    attribution += `
### Rejected Models
${arbiterDecision.rejected_models.map(m =>
  `- **${m.model}**: ${m.rejection_reason}`
).join('\n')}
`
  }

  if (arbiterDecision.issue_priority) {
    attribution += `
### Priority
**${arbiterDecision.issue_priority}** - Validated by multi-model consensus
`
  }

  if (includeTimestamp) {
    attribution += `
---
*Generated by AI Multi-Model Review on ${new Date().toISOString().split('T')[0]}*
`
  }

  return attribution
}

export function formatModelDecision(modelReview) {
  if (!modelReview) return 'N/A'

  let decision = ''

  // Handle different schema types
  if (modelReview.is_real_issue !== undefined) {
    decision = modelReview.is_real_issue ? 'Real Issue' : 'False Positive'
  } else if (modelReview.approval_recommendation) {
    decision = modelReview.approval_recommendation === 'approve' ? 'Approve' :
              modelReview.approval_recommendation === 'request_changes' ? 'Request Changes' :
              'Comment'
  } else if (modelReview.approach) {
    decision = 'Fix Proposed'
  } else {
    decision = 'Reviewed'
  }

  const confidence = modelReview.confidence || 0
  return `${decision} (Confidence: ${confidence}%)`
}

export function formatShortAttribution(reviews, arbiterDecision) {
  const { opus, sonnet, haiku, gemini } = reviews
  const allModels = [opus, sonnet, haiku, gemini].filter(Boolean)

  const agreeCount = allModels.filter(m => {
    if (arbiterDecision.final_decision === 'real_issue' || arbiterDecision.final_decision === 'approved') {
      return m.is_real_issue === true || m.approval_recommendation === 'approve'
    } else {
      return m.is_real_issue === false || m.approval_recommendation !== 'approve'
    }
  }).length

  return `🤖 **AI Review**: ${agreeCount}/${allModels.length} models agree | Consensus: ${arbiterDecision.consensus_score}% | Best: ${arbiterDecision.accepted_model}`
}

export function formatInlineComment(issue, reviews, arbiterDecision) {
  return `## 🤖 AI Code Review

**Issue**: ${issue.description}

${formatShortAttribution(reviews, arbiterDecision)}

**Models Detected This**:
${Object.entries(reviews).filter(([_, v]) => v).map(([model, review]) =>
  `- ${model.charAt(0).toUpperCase() + model.slice(1)}: ${review.confidence || 0}% confidence`
).join('\n')}

**Arbiter Verdict**: ${arbiterDecision.final_decision}

---
*Multi-model AI code review - ${arbiterDecision.consensus_score}% consensus*
`
}

export function formatPRComment(reviews, arbiterDecision, qualityScore) {
  const { opus, sonnet, haiku, gemini } = reviews

  return `## 🤖 AI Pull Request Review

### Quality Score: ${qualityScore}/100

### Multi-Model Consensus
${formatShortAttribution(reviews, arbiterDecision)}

### Detailed Reviews

**Opus** (${opus?.confidence || 0}% confidence):
- Recommendation: ${opus?.approval_recommendation || 'N/A'}
- Issues Found: ${opus?.issues_found?.length || 0}
${opus?.strengths ? `- Strengths: ${opus.strengths.slice(0, 2).join(', ')}` : ''}

**Sonnet** (${sonnet?.confidence || 0}% confidence):
- Recommendation: ${sonnet?.approval_recommendation || 'N/A'}
- Issues Found: ${sonnet?.issues_found?.length || 0}

**Haiku** (${haiku?.confidence || 0}% confidence):
- Recommendation: ${haiku?.approval_recommendation || 'N/A'}
- Issues Found: ${haiku?.issues_found?.length || 0}

${gemini ? `**Gemini** (${gemini?.confidence || 0}% confidence):
- Recommendation: ${gemini?.approval_recommendation || 'N/A'}
- Issues Found: ${gemini?.issues_found?.length || 0}
` : ''}

### Arbiter Decision
**${arbiterDecision.final_decision.toUpperCase()}** (${arbiterDecision.consensus_score}% consensus)

**Reasoning**: ${arbiterDecision.accepted_reasoning}

---
*AI-powered PR review - Multi-model consensus*
`
}
